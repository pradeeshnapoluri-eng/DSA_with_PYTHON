a=10
b=20
c=a+b
print(c) #space complexity is O(1) because we are using only a constant amount of space to store the variables a, b, and c. The space used does not depend on the size of the input, so it is constant.
print()
##
n=5
arr = []
for i in range(n):
    arr.append(i) #space complexity is O(n) because we are using a list to store n elements. The space used grows linearly with the size of the input, so it is linear.
print(arr)
print()
